package day10jan24;

public class Student {
	
		int rollNo;
		String name;
		public int getRollNo() {
			return rollNo;
		}
		 
		public String getName() {
			return name;
		}
		 
		public Student(int rollNo, String name) {
			super();
			this.rollNo = rollNo;
			this.name = name;
	}

}

